/* またこの関数を呼び出し、次のように表示するmain関数を作成してください。                        */
/*［結果の表示例］                                                                               */
/*・うるう年だった場合      :○○年は、うるう年です。                                            */
/*・うるう年ではなかった倍  :○○年は、うるう年ではありません。                                  */
